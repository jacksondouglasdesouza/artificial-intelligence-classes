'''

Receba os valores para:
Base e altura de um triângulo e em seguida, apresente o valor para o valor da área
formula:

-----------------------------
- ÁREA (BASE * ALTURA) / 2 -
-----------------------------

'''

print("Digite o valor da Base: ")
base = float(input())

print("Digite o valor da Altura: ")
altura = float(input())

area = (base * altura) / 2

print("A área do triângulo e de: ", area)


